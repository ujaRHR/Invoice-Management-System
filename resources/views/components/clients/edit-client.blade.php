<script>
    async function clientInfo() {
        let clientId = $('#updateClientId').val()

        axios.post('/client-info', {
            id: clientId
        }).then(function(response) {
            $('#updateFullname').val(response.data.client.fullname)
            $('#updateEmail').val(response.data.client.email)
            $('#updateCompany').val(response.data.client.company)
            $('#updateCountry').val(response.data.client.country)
        })
    }

    async function updateClient(event) {
        event.preventDefault();
        let clientId = parseInt($('#updateClientId').val())
        let fullname = $('#updateFullname').val();
        let email = $('#updateEmail').val();
        let company = $('#updateCompany').val();
        let country = $('#updateCountry').val();


        formData = {
            id: clientId,
            fullname: fullname,
            email: email,
            company: company,
            country: country
        }

        axios.post('/update-client', formData).then(function(response) {
            if (response.status == 200 && response.data.success == true) {
                toastr.success("Client updated successfully");
                $('#editBtnClose').click();
                getClients();
            } else {
                toastr.error("Something Went Wrong!");
            }
        });
    }
</script>
